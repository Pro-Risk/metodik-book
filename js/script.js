// Поиск по темам и контенту
document.getElementById('searchInput').addEventListener('input', function() {
    let query = this.value.toLowerCase();
    let accordionItems = document.querySelectorAll('.accordion-item');
    let content = document.getElementById('content');

    let found = false;

    accordionItems.forEach(item => {
        let themeButton = item.querySelector('.accordion-button').innerText.toLowerCase();
        let subHeadings = item.querySelectorAll('.accordion-body ul li a');

        // Поиск по темам
        if (themeButton.includes(query)) {
            item.style.display = ''; // Показываем тему
            found = true;
        } else {
            let subHeadingFound = false;
            // Поиск по подзаголовкам
            subHeadings.forEach(subHeading => {
                if (subHeading.innerText.toLowerCase().includes(query)) {
                    subHeadingFound = true;
                    found = true;
                }
            });

            // Если ничего не найдено, скрываем тему
            if (!subHeadingFound) {
                item.style.display = 'none';
            } else {
                item.style.display = ''; // Показываем тему, если найден подзаголовок
            }
        }
    });

    // Обновляем контент в основном блоке, если ничего не найдено
    if (!found) {
        content.innerHTML = "<p>Ничего не найдено по запросу.</p>";
    } else {
        content.innerHTML = "<p>Выберите тему или подзаголовок для просмотра контента.</p>";
    }
});

// Функция для генерации модального окна на основе данных рыбы
function generateFishModal(modalId, title, imgSrc, description, habitat, references) {
    const modalHtml = `
    <div class="modal fade" id="${modalId}" tabindex="-1" aria-labelledby="${modalId}Label" aria-hidden="true">
        <div class="modal-dialog">
            <div class="modal-content">
                <div class="modal-header">
                    <h5 class="modal-title" id="${modalId}Label">${title}</h5>
                    <button type="button" class="btn-close" data-bs-dismiss="modal" aria-label="Close"></button>
                </div>
                <div class="modal-body">
                    <img src="${imgSrc}" alt="${title}" class="img-fluid">
                    <p>${description}</p>
                    <h5 align="center"><strong>Таралуы:</strong></h5>
                    <p align="center">${habitat}</p><br>
                    <h5 align="center"><strong>Пайдаланылған әдебиеттер:</strong></h5>
                    <p align="center">${references}</p>
                </div>
            </div>
        </div>
    </div>`;

    document.body.insertAdjacentHTML('beforeend', modalHtml);
}

// Функция для добавления обработчика события открытия модального окна
function addModalTrigger(triggerSelector, modalId) {
    const triggers = document.querySelectorAll(triggerSelector);
    triggers.forEach(trigger => {
        trigger.addEventListener('click', function() {
            const modal = new bootstrap.Modal(document.getElementById(modalId));
            modal.show();
        });
    });
}

